/* USART1 ---------------------------------------------------------------------------*/

#define EN_USART1_RX 			1			//使能（1）/禁止（0）串口1接收
#if EN_USART1_RX

#define USART_REC_LEN  			200  		//定义最大接收字节数 200
	  	
extern uint8_t  USART_RX_BUF[USART_REC_LEN]; 	//接收缓冲,最大USART_REC_LEN个字节.末字节为换行符 
extern uint16_t USART_RX_STA;         			//接收状态标记	
extern UART_HandleTypeDef UART1_Handler; 	//UART句柄

#define RXBUFFERSIZE   1 					//缓存大小
extern uint8_t aRxBuffer[RXBUFFERSIZE];			//HAL库USART接收Buffer

#endif /* EN_USART1_RX */

/* USART2 ---------------------------------------------------------------------------*/

#define EN_USART2_RX			(1)			// 使能串口二接收
#if EN_USART2_RX

#define USART2_REC_LEN  		(200)  		// 定义最大接收字节数 200

extern uint8_t USART2_RX_BUF[USART2_REC_LEN]; 	//接收缓冲,最大USART2_REC_LEN个字节
#define USART2_RX_STA_REC_END			(0X8000)		// bit15		接收完成一包数据
#define USART2_RX_STA_HAS_REC			(0X4000)		// bit14		有接收
#define USART2_RX_STA_OVERFLOW			(0X3000)		// bit13~12		缓冲区溢出次数
#define USART2_RX_STA_REC_NUM			(0X0FFF)		// bit11~0		缓冲区接收字符数
extern uint16_t USART2_RX_STA;         			//接收状态标记	
extern UART_HandleTypeDef UART2_Handler; 		//UART2句柄

#define _2RXBUFFERSIZE   		(1)			// 缓存大小
extern uint8_t _2RxBuffer[_2RXBUFFERSIZE];

#endif /* EN_USART2_RX */

